    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        // TODO add your handling code here:
        if (ValidaValores.NomeIconeNaoExiste(jTextFieldNome.getText()) && ValidaValores.validaNomeIcone(jTextFieldNome.getText()) && ValidaValores.validaDouble(jTextFieldBanda.getText()) && ValidaValores.validaDouble(jTextFieldTaxaOcupacao.getText()) && ValidaValores.validaDouble(jTextFieldLatencia.getText())) {
            this.setVisible(false);
            I.setNome(jTextFieldNome.getText());
            ValidaValores.addNomeIcone(jTextFieldNome.getText());
            I.setLatencia(Double.parseDouble(jTextFieldLatencia.getText()));
            I.setBanda(Double.parseDouble(jTextFieldBanda.getText()));
            I.setTaxaOcupacao(Double.parseDouble(jTextFieldTaxaOcupacao.getText()));
            //I.setPosition(Integer.parseInt(jTextFieldX.getText()),Integer.parseInt(jTextFieldY.getText()));
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

